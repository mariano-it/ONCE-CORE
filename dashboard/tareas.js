function renderTareas() {

  const container =
    document.getElementById("tareasContainer");

  if (!container) return;

  let tareas = tareasCache;

  if (currentTab === "mias") {

    tareas =
      tareas.filter(
        t => t.autor_id === currentUser.email
      );

  }

  if (tareas.length === 0) {

    container.innerHTML =
      `<div class="tareas-empty">
        Sin tareas publicadas
       </div>`;

    return;

  }

  container.innerHTML =
    tareas.map(buildCard).join("");

  bindEvents();

}


