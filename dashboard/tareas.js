function buildCard(t) {

  const isOwn =
    t.autor_id === currentUser.email;

  const suertes =
    t.suertes?.length || 0;

  const ayudas =
    t.ayudas?.length || 0;

  return `

    <div class="tarea-card">

      <div class="tarea-title">
        ${escapeHtml(t.title)}
      </div>

      <div class="tarea-author">
        ${escapeHtml(t.autor_name)}
      </div>

      <div class="tarea-actions">

        ${
          !isOwn
          ? `
          <button
            class="btn-suerte"
            data-id="${t.id}">
            ğŸ’› ${suertes}
          </button>

          <button
            class="btn-ayuda"
            data-id="${t.id}">
            ğŸ¤ ${ayudas}
          </button>
          `
          : `<span>Tuya</span>`
        }

      </div>

    </div>

  `;

}


