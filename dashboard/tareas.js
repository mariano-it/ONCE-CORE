// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

let supabase = null;

let currentUser = null;

let tareasCache = [];

let currentTab = "mias";

let selectedPriority = "normal";


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Esperar Supabase
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function waitForSupabase() {

  return new Promise(resolve => {

    const interval = setInterval(() => {

      if (window._supabase) {

        clearInterval(interval);

        resolve(window._supabase);

      }

    }, 50);

  });

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// InicializaciÃ³n principal
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

document.addEventListener("DOMContentLoaded", async () => {

  try {

    supabase = await waitForSupabase();

    await loadUser();

    setupUI();

    await loadTareas();

    subscribeRealtime();

    console.log("ONCE CORE tareas inicializado");

  } catch (err) {

    console.error("Error inicializando:", err);

  }

});


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Usuario actual
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

async function loadUser() {

  const { data, error } =
    await supabase.auth.getSession();

  if (error || !data.session) {

    window.location.href = "../login/login.html";

    return;

  }

  currentUser = data.session.user;

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// UI setup
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function setupUI() {

  UI.publishBtn?.addEventListener(
    "click",
    publishTarea
  );

  UI.tabMias?.addEventListener(
    "click",
    () => switchTab("mias")
  );

  UI.tabTodas?.addEventListener(
    "click",
    () => switchTab("todas")
  );

  document
    .querySelectorAll(".priority-btn")
    .forEach(btn => {

      btn.onclick = () => {

        document
          .querySelectorAll(".priority-btn")
          .forEach(b =>
            b.classList.remove("active")
          );

        btn.classList.add("active");

        selectedPriority = btn.dataset.p;

      };

    });

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Cargar tareas
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

async function loadTareas() {

  const { data, error } =
    await supabase
      .from("tareas")
      .select("*")
      .order("created_at", { ascending: false });

  if (error) {

    console.error(error);

    showToast("Error cargando tareas");

    return;

  }

  tareasCache = data;

  renderTareas();

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Crear tarea
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

async function publishTarea() {

  const title =
    UI.ntTitle.value.trim();

  if (!title) {

    showToast("Escribe un tÃ­tulo");

    return;

  }

  const tarea = {

    title: title,

    subject:
      UI.ntSubject.value || "General",

    description:
      UI.ntDesc.value || "",

    due:
      UI.ntDue.value || null,

    priority:
      selectedPriority,

    autor_id:
      currentUser.id,

    autor_email:
      currentUser.email,

    autor_name:
      currentUser.user_metadata?.name
      || currentUser.email,

    suertes: [],

    ayudas: [],

    done: false

  };

  const { error } =
    await supabase
      .from("tareas")
      .insert(tarea);

  if (error) {

    console.error(error);

    showToast("Error publicando");

    return;

  }

  clearForm();

  showToast("Tarea publicada");

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Render tareas
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function renderTareas() {

  if (!UI.container) return;

  let tareas = tareasCache;

  if (currentTab === "mias") {

    tareas =
      tareas.filter(
        t => t.autor_id === currentUser.id
      );

  }

  if (tareas.length === 0) {

    UI.container.innerHTML =
      `<div class="tareas-empty">
        Sin tareas
      </div>`;

    return;

  }

  UI.container.innerHTML =
    tareas.map(buildCard).join("");

  bindEvents();

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Card HTML
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function buildCard(t) {

  const isOwn =
    t.autor_id === currentUser.id;

  const suertes =
    t.suertes?.length || 0;

  const ayudas =
    t.ayudas?.length || 0;

  return `

  <div class="tarea-card">

    <div class="tarea-title">
      ${escapeHtml(t.title)}
    </div>

    <div class="tarea-author">
      ${escapeHtml(t.autor_name)}
    </div>

    <div class="tarea-actions">

      ${
        isOwn
        ? `<span>Tuya</span>`
        : `
          <button
            class="btn-suerte"
            data-id="${t.id}">
            ğŸ’› ${suertes}
          </button>

          <button
            class="btn-ayuda"
            data-id="${t.id}">
            ğŸ¤ ${ayudas}
          </button>
        `
      }

    </div>

  </div>

  `;

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Eventos botones
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function bindEvents() {

  document
    .querySelectorAll(".btn-suerte")
    .forEach(btn => {

      btn.onclick =
        () => sendSuerte(btn.dataset.id);

    });

  document
    .querySelectorAll(".btn-ayuda")
    .forEach(btn => {

      btn.onclick =
        () => sendAyuda(btn.dataset.id);

    });

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Suerte
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

async function sendSuerte(id) {

  const tarea =
    tareasCache.find(t => t.id == id);

  if (!tarea) return;

  const suertes =
    tarea.suertes || [];

  if (
    suertes.includes(currentUser.id)
  ) return;

  suertes.push(currentUser.id);

  await supabase
    .from("tareas")
    .update({ suertes })
    .eq("id", id);

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Ayuda
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

async function sendAyuda(id) {

  const tarea =
    tareasCache.find(t => t.id == id);

  if (!tarea) return;

  const ayudas =
    tarea.ayudas || [];

  ayudas.push({

    user_id: currentUser.id,

    email: currentUser.email,

    ts: Date.now()

  });

  await supabase
    .from("tareas")
    .update({ ayudas })
    .eq("id", id);

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Realtime
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function subscribeRealtime() {

  supabase
    .channel("tareas_channel")

    .on(

      "postgres_changes",

      {
        event: "*",
        schema: "public",
        table: "tareas"
      },

      () => loadTareas()

    )

    .subscribe();

}


// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Helpers
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function switchTab(tab) {

  currentTab = tab;

  renderTareas();

}

function clearForm() {

  UI.ntTitle.value = "";
  UI.ntSubject.value = "";
  UI.ntDue.value = "";
  UI.ntDesc.value = "";

}

function escapeHtml(text) {

  if (!text) return "";

  return text
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");

}

function showToast(msg) {

  console.log("[ONCE CORE]", msg);

}
